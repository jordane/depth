function render() {
  var x, y;

  for (y = 0; y < 15; y++) {
    for (x = 0; x < 20; x++) {
      ctx.save();
      ctx.translate(x * 32, y * 32);
      sprites[map[y][x]].render(ctx);
      ctx.restore();
    }
  }

  requestAnimFrame(render);
}

